#include <unistd.h>

int	main(int argc, char **argv)
{
	int	c;
	int	d;

	c = argc - 1;
	d = 0;
	if (argc > 1)
	{
		while (c > 0)
		{
			d = 0;
			while (argv[c][d] != '\0')
			{
				write(1, &argv[c][d], 1);
				d++;
			}
			write(1, "\n", 1);
			c--;
		}
	}
	return (0);
}

/*
exactly the same as 01 but in reverse
C-- and d = 0 (d is our string) counting down to the end of our strings
*/
